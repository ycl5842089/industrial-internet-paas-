package cn.chinaunicom.industrialinternet.entitys.businessDynamic;

public class BusinessDynamicVO {
    private   String   businessDynamicId;

    private   String   businessDynamicText;

    private   String   publishDate;

    public String getBusinessDynamicId() {
        return businessDynamicId;
    }

    public void setBusinessDynamicId(String businessDynamicId) {
        this.businessDynamicId = businessDynamicId;
    }

    public String getBusinessDynamicText() {
        return businessDynamicText;
    }

    public void setBusinessDynamicText(String businessDynamicText) {
        this.businessDynamicText = businessDynamicText;
    }

    public String getPublishDate() {
        return publishDate;
    }

    public void setPublishDate(String publishDate) {
        this.publishDate = publishDate;
    }
}
